select WELL_DIR_SRVY.UWI as "Well ID",
    WELL_DIR_SRVY.COMPUTE_METHOD as "Calculation Method",
    WELL_DIR_SRVY.MAGNETIC_DECLINATION as "Magnetic Declination",
    WELL_DIR_SRVY.GX_KOP_MD as "KOP MD",
    WELL_DIR_SRVY.GX_BASE_TVD as "BH TVD",
    WELL_DIR_SRVY.GX_BASE_E_W_OFFSET as "BH E/W Offset",
    WELL_DIR_SRVY.GX_BASE_N_S_OFFSET as "BH N/S Offset",
    WELL_DIR_SRVY.GX_BASE_LONGITUDE as "BH Longitude",
    WELL_DIR_SRVY.GX_BASE_LATITUDE as "BH Latitude",
    DBA.GET_CLOSURE(WELL_DIR_SRVY.GX_BASE_N_S_OFFSET,WELL_DIR_SRVY.GX_BASE_E_W_OFFSET) as Closure,
    WELL_DIR_SRVY.GX_FOOTAGE as Footage,
    WELL_DIR_SRVY.GX_BASE_LOCATION_STRING as "S/T/R",
    WELL_DIR_SRVY.NORTH_REFERENCE as "North Reference",
    WELL_DIR_SRVY_STATION.STATION_MD as MD,
    WELL_DIR_SRVY_STATION.STATION_TVD as TVD,
    WELL_DIR_SRVY_STATION.INCLINATION as Inclination,
    WELL_DIR_SRVY_STATION.AZIMUTH as Azimuth,
    WELL_DIR_SRVY_STATION.NS_DIRECTION as "N/S",
    WELL_DIR_SRVY_STATION.Y_OFFSET as "N/S Offset",
    WELL_DIR_SRVY_STATION.EW_DIRECTION as "E/W",
    WELL_DIR_SRVY_STATION.X_OFFSET as "E/W Offset",
    WELL_DIR_SRVY_STATION.GX_STATION_LATITUDE as "Lat/X",
    WELL_DIR_SRVY_STATION.GX_STATION_LONGITUDE as "Long/Y",
    WELL_DIR_SRVY_STATION.SOURCE as Source,
    WELL_DIR_SRVY_STATION.SURVEY_ID as "Survey ID",
    WELL_DIR_SRVY_STATION.STATION_ID as "Survey Point Number"
    from DBA.WELL_DIR_SRVY left outer join DBA.WELL_DIR_SRVY_STATION on(WELL_DIR_SRVY.UWI = WELL_DIR_SRVY_STATION.UWI) 
    ORDER BY "Well ID", "MD", "Survey ID";
